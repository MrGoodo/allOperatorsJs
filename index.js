// Сложение //
function Addition(a, b) {
  return a + b;
}

// Вычитание //
function Subtraction(a, b) {
  return a - b;
}

// Умножение //
function Multiplication(a, b) {
  return a * b;
}

// Степень //
function Exponentiation(a, b) {
  return a ** b;
}

// Деление //
function Division(a, b) {
  return a / b;
}

// модуль //
function Modulus(a, b) {
  return a % b;
}

// Увеличеие //
function Increment(a) {
  return ++a;
}

// Уменьшение //
function Decrement(a) {
  return --a;
}
